drop table if exists PRODUCT;
CREATE TABLE PRODUCT(
  PROD_ID INTEGER NOT NULL,
  PROD_NAME VARCHAR(30) NOT NULL,
  PROD_DESCRIPTION VARCHAR(100) NOT NULL,
  PROD_PRICE DECIMAL NOT NULL,
  PROD_QOH DECIMAL NOT NULL,
  PRIMARY KEY (PROD_ID)
);

drop table if exists CUSTOMER;
CREATE TABLE CUSTOMER(
  CUST_ID INTEGER NOT NULL,
  CUST_NAME VARCHAR (20) NOT NULL,
  CUST_GENDER VARCHAR (8) NOT NULL,
  CUST_EMAIL VARCHAR (30) NOT NULL,
  PRIMARY KEY (CUST_ID)
);

drop table if exists SUPPLIER;
CREATE TABLE SUPPLIER(
  SUPPLIER_ID INTEGER NOT NULL,
  SUPPLIER_NAME VARCHAR (20) NOT NULL,
  SUPPLIER_LOCATION VARCHAR (20) NOT NULL,
  PRIMARY KEY (SUPPLIER_ID)
);

drop table if exists INVOICE;
CREATE TABLE INVOICE(
  INVOICE_ID INTEGER NOT NULL,
  INVOICE_BALANCE DECIMAL NOT NULL,
  INVOICE_DATE DATE NOT NULL,
  PRIMARY KEY(INVOICE_ID)
);

drop table if exists SALE;
CREATE TABLE SALE(
  SALE_ID INTEGER NOT NULL,
  PROD_ID INTEGER NOT NULL,
  CUST_ID INTEGER NOT NULL,
  INVOICE_ID INTEGER NOT NULL,
  SALE_QUANTITY INTEGER NOT NULL,
  FOREIGN KEY (PROD_ID) REFERENCES PRODUCT(PROD_ID),
  FOREIGN KEY (CUST_ID) REFERENCES CUSTOMER(CUST_ID),
  FOREIGN KEY (INVOICE_ID) REFERENCES INVOICE(INVOICE_ID),
  PRIMARY KEY(SALE_ID)
);

drop table if exists PURCHASE;
CREATE TABLE PURCHASE (
  PURCHASE_ID INTEGER NOT NULL,
  PROD_ID INTEGER NOT NULL,
  SUPPLIER_ID INTEGER NOT NULL,
  INVOICE_ID INTEGER NOT NULL,
  PURCHASE_QUANTITY INTEGER NOT NULL,
  PRIMARY KEY (PURCHASE_ID),
  FOREIGN KEY (PROD_ID) REFERENCES PRODUCT(PROD_ID),
  FOREIGN KEY (SUPPLIER_ID) REFERENCES SUPPLIER(SUPPLIER_ID),
  FOREIGN KEY (INVOICE_ID) REFERENCES INVOICE(INVOICE_ID)
);

drop table if exists ADVERTISEMENT;
CREATE TABLE ADVERTISEMENT(
  AD_ID INTEGER NOT NULL,
  AD_NAME VARCHAR (50) NOT NULL,
  AD_DESCRIPTION VARCHAR (200) NOT NULL,
  PRIMARY KEY (AD_ID)
);

drop table if exists ENGAGEMENT;
CREATE TABLE ENGAGEMENT(
  AD_ID INTEGER NOT NULL,
  CUST_ID INTEGER NOT NULL,
  PRIMARY KEY (AD_ID, CUST_ID),
  FOREIGN KEY (AD_ID) REFERENCES ADVERTISEMENT(AD_ID),
  FOREIGN KEY (CUST_ID) REFERENCES CUSTOMER(CUST_ID)
);






